/* notation:

        G is the generator 
        d is the private key
        Q is the public key (Q = d*G)
        n is the order of G
*/

struct xc_wb_ECC_public_key {
    XC_uint32 sanity_check;
    XC_uint32 id;
    XC_BigNum Qx;
    XC_BigNum Qy;
    XC_int32  mod_wlength; /* number of words needed to hold the modulus */
    XC_int32  keysize; /* number of octets needed to hold n = ord(G) */
};

struct xc_wb_ECC_private_key {
    XC_uint32  sanity_check;
    XC_uint32  id;
    XC_BigNum  d;  /* private exponent */
    XC_int32   d_wlength; /* number of words needed to hold d */
    XC_Octet * dprime;
    XC_int32   dprimelength; /* length in octets of dprime */
    XC_int32   keysize; /* number of octets needed to hold n = ord(G) */
};

